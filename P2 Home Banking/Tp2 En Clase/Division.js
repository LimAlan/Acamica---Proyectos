function dividir(Numero1, Numero2) {

    if (isNaN == Numero1 || isNan == Numero2) {
        alert("Alguno de los valores no es un numero");
    }
    else {

        if (Numero2 == 0) {
            alert("Error, no se puede dividir por 0");

        }
        else { var resultado = Numero1 / Numero2; }
    }

    console.log(resultado)
}
